package modelo;

public class MateriaVO {
	private String codmat;
	private String matnom;
	private int anio;
	private String plan;

	private int modulos;
	public String getCodmat() {
		return codmat;
	}
	public void setCodmat(String codmat) {
		this.codmat = codmat;
	}
	public String getMatnom() {
		return matnom;
	}
	public void setMatnom(String matnom) {
		this.matnom = matnom;
	}
	public int getAnio() {
		return anio;
	}
	public void setAnio(int anio) {
		this.anio = anio;
	}
	public String getPlan() {
		return plan;
	}
	public void setPlan(String plan) {
		this.plan = plan;
	}

	public int getModulos() {
		return modulos;
	}
	public void setModulos(int modulos) {
		this.modulos = modulos;
	}
	
	
}
